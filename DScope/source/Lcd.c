/*******************************************************************************
Point_SCR: Set display position   Input: X, Y
*******************************************************************************/
void Point_SCR(unsigned int x0, unsigned int y0)
{
  LCD_WR_REG(0x0020,y0);
  LCD_WR_REG(0x0021,x0);
  LCD_DATA_OUT=0x0022;  //DRAM Reg.
  LCD_RS_LOW();
  LCD_nWR_LOW();
  LCD_nWR_HIGH();
  LCD_nWR_LOW();
  LCD_nWR_HIGH();
  LCD_RS_HIGH();
}
/*******************************************************************************
 Set_Pixel: Set a Pixel  Input: Color
*******************************************************************************/
void Set_Pixel(unsigned int Color)
{
  LCD_DATA_OUT=Color;   //Color Data
  LCD_nWR_LOW();
  LCD_nWR_HIGH();
}

