/*******************************************************************************
 Display_Char: Display Char   Input: X,Y, Color, Mode, char
*******************************************************************************/
void Display_Char(unsigned int x0, unsigned int y0, unsigned int Color, unsigned char Mode, char s)
{
  signed int i, j, b;
  
  Point_SCR(x0, y0);
  for (j=0; j<14;++j){
    if(Mode==0) Set_Pixel(BLACK);   //Normal replace Display
    if(Mode==1) Set_Pixel(Color);   //Inverse replace Display
  }
  
  x0++;
  if (s!=0) {
    unsigned const int *scanline=Char_Dot+((s-0x22)*8);
    for(i=0;i<8;++i){
      Point_SCR(x0+i, y0);
      if((s==0x20)||(s==0x21)) b=0x0000;
      else                       b=scanline[i];
      if((s==0x21)&&(i==4)) break;
      for(j=0;j<14;++j){
        if(b&4) {
          if(Mode==0) Set_Pixel(Color);
          if(Mode==1) Set_Pixel(BLACK);
          if(Mode==2) Set_Pixel(Color);
          if(Mode==3) Set_Pixel(BLACK);
        } else {
          if(Mode==0) Set_Pixel(BLACK);
          if(Mode==1) Set_Pixel(Color);
          if(Mode==2) Set_Pixel(BLACK);
          if(Mode==3) Set_Pixel(Color);
        }
        b>>=1;
      }
    }
    if(s==0x21) x0 +=4;
    else  x0 += 8;
  }
}

